package GeoLocalizacao.com.Avaliacao02.Geolocalizacao;

public class Coordenadas {

        private double lat;
        private double lon;

        // Getters latitude
        public double getLat() {
            return lat;
        }
        // Setter latitude
        public void setLat(double lat) {
            this.lat = lat;
        }

        // Getters longitude
        public double getLon() {
            return lon;
        }
        // Setter longitude
        public void setLon(double lon) {
            this.lon = lon;
        }
}
